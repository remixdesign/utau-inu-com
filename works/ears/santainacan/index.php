	<h3>Production Notes</h3>
	
	<div class="works_text">
		<div class="works_pic"><img src="../../../images/works_icon_boo.jpg" width="100" height="100" alt="Brian Stewart photo" /></div>
		<p><strong>boo</strong><br /> 
		When Roger approached me about performing for the Jrock Choice Awards, my first instinct was to do a cover of a Jrock song. I brought Ying several songs including the Back Horn's "Hane ~Yozora wo Koete" but he rejected everything. One day I was humming a few bars of what would become the prechorus and I came up with this silly phrase "Konya no Menu ha Tonakainiku (Tonight's Menu is reindeer meat)" When I first brought it to Ying I had only that prechorus and some of the verses. My initial idea for the song was much swingier with a swagger like Sophia's Matsuoka but Ying pushed it in a darker direction which better suited the lyrics I wrote which examine the impossible standards of behavior set by the holidays. It's an anti-Christmas song to liven up your anti-Christmas parties. There's a whole lotta doofiness hidden in the zombie chorus of Na Na Nas. </p>
